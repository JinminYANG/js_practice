function addNum(x, y, z) { // x, y, z라는 3개의 매개변수를 가지는 함수 addNum()을 정의
    return x + y + z;
}

console.log(addNum(1, 2, 3));  // 인수로 1, 2, 3을 전달하여 함수를 호출

console.log(addNum(1, 2));   // 인수로 1, 2을 전달하여 함수를 호출

console.log(addNum(1));   // 인수로 1을 전달하여 함수를 호출

console.log(addNum());   // 인수로 아무것도 전달하지 않고 함수를 호출


